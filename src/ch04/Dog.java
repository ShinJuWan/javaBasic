package ch04;

// 클래스 - 여러가지 특징(상태)을 가지고 있다.
// int, String -> 한가지의 상태만 가질 수 있다.
public class Dog {
	int age = 20; // 변경 가능
	String name = "토토"; // 변경 가능
	String color = "하얀색"; // 변경 가능
	final String type = "말티즈"; // 변경 불가 변수는 final을 입력하여 상수로 만든다. 
}

// 변수 = 필드 = 전역변수 = 힙변수 = 상태
// 전역변수 (지역변수가 아니다. 메서드 내부에서 만들어진 변수가 아니다.)